// Current language (default: Azerbaijani)
let currentLang = localStorage.getItem('datarover_lang') || 'az';

// Build reverse lookup for AZ->EN translation
const reverseTranslations = {};
Object.keys(translations.az).forEach(enKey => {
    const azValue = translations.az[enKey];
    reverseTranslations[azValue] = enKey;
});

// Translation function with reverse lookup support
function t(key) {
    // If current language has this key, use it
    if (translations[currentLang]?.[key]) {
        return translations[currentLang][key];
    }
    
    // If key is actually an AZ value and we need EN, do reverse lookup
    if (currentLang === 'en' && reverseTranslations[key]) {
        return reverseTranslations[key];
    }
    
    // Return the key as fallback
    return key;
}

// Switch language function
function switchLanguage(lang) {
    if (!translations[lang]) {
        console.warn('Language not supported:', lang);
        return;
    }
    
    currentLang = lang;
    localStorage.setItem('datarover_lang', lang);
    
    // Update UI
    document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
    document.querySelector('.lang-btn[data-lang="' + lang + '"]')?.classList.add('active');

    // Update global search placeholder
    const globalSearchInput = document.getElementById('globalSearchInput');
    if (globalSearchInput) {
        globalSearchInput.placeholder = t('Discover your data universe');
    }

    // Translate menu items
    document.querySelectorAll('.menu-item').forEach(item => {
        const page = item.getAttribute('data-page');
        const icon = item.querySelector('svg');
        const iconHTML = icon ? icon.outerHTML : '';
        
        if (page === 'dashboard') item.innerHTML = iconHTML + ' ' + t('Dashboard');
        else if (page === 'glossary') item.innerHTML = iconHTML + ' ' + t('Business Glossary');
        else if (page === 'catalog') item.innerHTML = iconHTML + ' ' + t('Data Catalog');
        else if (page === 'external') item.innerHTML = iconHTML + ' ' + t('External Sources');
        else if (page === 'reports') item.innerHTML = iconHTML + ' ' + t('Reporting Catalog');
        else if (page === 'quality') item.innerHTML = iconHTML + ' ' + t('Data Quality');
        else if (page === 'lineage') item.innerHTML = iconHTML + ' ' + t('Data Lineage');
    });
    
    // Reload current page to apply translations
    const activeMenu = document.querySelector('.menu-item.active');
    if (activeMenu) {
        const currentPage = activeMenu.getAttribute('data-page');
        // Map page names to their actual load functions
        const pageLoaders = {
            'dashboard': 'loadDashboard',
            'glossary': 'loadGlossary',
            'catalog': 'loadCatalog',
            'external': 'loadExternalSources',
            'reports': 'loadReports',
            'quality': 'loadQuality',
            'lineage': 'loadLineage',
            'mapper': 'loadColumnMapper',
            'governance': 'loadGovernance',
            'ai': 'loadAIAssistant',
            'users': 'loadUserManagement',
            'roles': 'loadRoleManagement',
            'audit': 'loadAuditLog',
            'settings': 'loadSettings'
        };
        
        const loaderFunc = pageLoaders[currentPage];
        if (loaderFunc && typeof window[loaderFunc] === 'function') {
            window[loaderFunc]();
        } else {
            loadDashboard();
        }
    } else {
        loadDashboard();
    }
}

// Get available languages
function getAvailableLanguages() {
    return Object.keys(translations).map(code => ({
        code: code,
        name: code === 'az' ? 'Azərbaycan' : code === 'en' ? 'English' : code.toUpperCase()
    }));
}
